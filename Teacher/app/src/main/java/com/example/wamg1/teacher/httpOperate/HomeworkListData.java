package com.example.wamg1.teacher.httpOperate;

public class HomeworkListData {
    private String title;
    private String yijiaCount;
    private String weijiaoCount;
    private String homeworkId;

    public String getHomeworkId() {
        return homeworkId;
    }

    public void setHomeworkId(String homeworkId) {
        this.homeworkId = homeworkId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getYijiaCount() {
        return yijiaCount;
    }

    public void setYijiaCount(String yijiaCount) {
        this.yijiaCount = yijiaCount;
    }

    public String getWeijiaoCount() {
        return weijiaoCount;
    }

    public void setWeijiaoCount(String weijiaoCount) {
        this.weijiaoCount = weijiaoCount;
    }
}
