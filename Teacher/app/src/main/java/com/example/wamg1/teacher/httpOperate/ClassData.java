package com.example.wamg1.teacher.httpOperate;

public class ClassData {
    String classname;
    String classId;
    String classStudentCount;
    String teacherName;

    public String getClassname() {
        return classname;
    }

    public void setClassname(String classname) {
        this.classname = classname;
    }

    public String getClassId() {
        return classId;
    }

    public void setClassId(String classId) {
        this.classId = classId;
    }

    public String getClassStudentCount() {
        return classStudentCount;
    }

    public void setClassStudentCount(String classStudentCount) {
        this.classStudentCount = classStudentCount;
    }

    public String getTeacherName() {
        return teacherName;
    }

    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }

}
